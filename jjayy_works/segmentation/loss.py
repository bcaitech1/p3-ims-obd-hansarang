_criterion_entrypoints = {
    'cross_entropy': nn.CrossEntropyLoss,
    'focal': FocalLoss,
    'label_smoothing': LabelSmoothingLoss,
    'f1': F1Loss,
    'f1_3': F1Loss_3,
    'f1_2': F1Loss_2
}


def criterion_entrypoint(criterion_name):
    return _criterion_entrypoints[criterion_name]


def is_criterion(criterion_name):
    return criterion_name in _criterion_entrypoints


def create_criterion(criterion_name, **kwargs):
    if is_criterion(criterion_name):
        create_fn = criterion_entrypoint(criterion_name)
        criterion = create_fn(**kwargs)
    else:
        raise RuntimeError('Unknown loss (%s)' % criterion_name)
    return criterion

def custom_loss(output, target, k):
    '''
    0: DiceLoss
    1: SoftCrossEntropyLoss
    2: FocalLoss
    3: LovaszLoss
    '''
    if k == 0:
        loss = smp.losses.DiceLoss("multiclass")
    elif k == 1:
        loss = smp.losses.SoftCrossEntropyLoss()
    elif k == 2:
        loss = smp.losses.FocalLoss("multiclass")
    elif k == 3:
        loss = smp.losses.LovaszLoss("multiclass")
    else:
        raise RuntimeError('Unknown loss')    
    return loss


def custom_loss1(output, target):
    mask_loss = nn.CrossEntropyLoss()(output[0], target[0])
    gender_loss = nn.CrossEntropyLoss()(output[1], target[1])
    age_loss = nn.CrossEntropyLoss()(output[2], target[2])
    return 0.14*mask_loss + 0.16*gender_loss + 0.7*age_loss, mask_loss.item(), gender_loss.item(), age_loss.item()

def custom_loss2(output, target):
    criterion1 = create_criterion("focal")
    criterion2 = create_criterion("f1_2")
    criterion3 = create_criterion("f1_3")
    mask_loss1 = criterion1(output[0], target[0])
    mask_loss2 = criterion3(output[0], target[0])
    gender_loss1 = criterion1(output[1], target[1])
    gender_loss2 = criterion2(output[1], target[1])
    age_loss1 = criterion1(output[2], target[2])
    age_loss2 = criterion3(output[2], target[2])
    mask_loss = (mask_loss1 + mask_loss2)/2
    gender_loss = (gender_loss1 + gender_loss2)/2
    age_loss = (age_loss1 + age_loss2)/2
    return 0.14*mask_loss + 0.16*gender_loss + 0.7*age_loss, mask_loss.item(), gender_loss.item(), age_loss.item()